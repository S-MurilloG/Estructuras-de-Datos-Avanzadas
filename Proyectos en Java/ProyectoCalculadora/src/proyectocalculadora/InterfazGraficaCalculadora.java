    private void jEquActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jEquActionPerformed

        try{
            //Toma el texto en pantalla
            res = jPantalla.getText();
            //Separa la cadena para que pueda analizarse
            arr = calc.separador(res);
            //Evalua usando la calculadora
            res = calc.evalua(arr);
            //Pone el texto correcto en pantalla
            jPantalla.setText(res);
            //Restaura los valores iniciales de las variables y sus jTextField
            jXTxt.setEnabled(false);
            jYTxt.setEnabled(false);
            jZTxt.setEnabled(false);
            jEvalVar.setEnabled(false);
            punto = false;
        } 
        catch(Exception e){
            jPantalla.setText("Syntax error");
        }
        
    }//GEN-LAST:event_jEquActionPerformed

