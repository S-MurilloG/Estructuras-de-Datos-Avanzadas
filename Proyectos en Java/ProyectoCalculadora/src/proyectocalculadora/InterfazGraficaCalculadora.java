    private void jEvalVarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jEvalVarActionPerformed
        String cad;
        String x,y,z;
        
        try{
            x=jXTxt.getText();
            y=jYTxt.getText();
            z=jZTxt.getText();
            //Toma el texto de la pantalla
            cad=jPantalla.getText();
            //Asigna las variables x, y, z en la expresión
            arr=calc.asigVar(cad, x, y, z);
            //Evalúa con los valores dados
            res = calc.evalua(arr);
            //Regresa el valor total a la pantalla
            jPantalla.setText(res);

            //Regresa los Botones y Labels a su estado inicial.
            jEqu.setEnabled(true);
            jEvalVar.setEnabled(false);
            jXTxt.setEnabled(false);
            jXTxt.setText("");
            jYTxt.setEnabled(false);
            jYTxt.setText("");
            jZTxt.setEnabled(false);
            jZTxt.setText("");
            punto = false;
            neg = false;
        }
        catch(NumberFormatException e){
            jPantalla.setText("Entrada inválida");
        }
    }//GEN-LAST:event_jEvalVarActionPerformed

